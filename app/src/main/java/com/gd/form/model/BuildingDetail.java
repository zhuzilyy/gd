package com.gd.form.model;

public class BuildingDetail {
    private String conditiondesc;
    private String creator;
    private String id;
    private String locate;
    private String solutionrecord;
    private int pipeid;
    private int stakeid;

    public String getConditiondesc() {
        return conditiondesc;
    }

    public void setConditiondesc(String conditiondesc) {
        this.conditiondesc = conditiondesc;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLocate() {
        return locate;
    }

    public void setLocate(String locate) {
        this.locate = locate;
    }

    public String getSolutionrecord() {
        return solutionrecord;
    }

    public void setSolutionrecord(String solutionrecord) {
        this.solutionrecord = solutionrecord;
    }

    public int getPipeid() {
        return pipeid;
    }

    public void setPipeid(int pipeid) {
        this.pipeid = pipeid;
    }

    public int getStakeid() {
        return stakeid;
    }

    public void setStakeid(int stakeid) {
        this.stakeid = stakeid;
    }
}
