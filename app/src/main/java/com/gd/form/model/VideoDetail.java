package com.gd.form.model;

public class VideoDetail {
    private String confirmer;
    private String creator;
    private String dealprocess;
    private String dealresult;
    private String exceptiondesc;
    private String highwarncode;
    private String id;
    private String locate;
    private int pipeid;
    public String getConfirmer() {
        return confirmer;
    }

    public void setConfirmer(String confirmer) {
        this.confirmer = confirmer;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public String getDealprocess() {
        return dealprocess;
    }

    public void setDealprocess(String dealprocess) {
        this.dealprocess = dealprocess;
    }

    public String getDealresult() {
        return dealresult;
    }

    public void setDealresult(String dealresult) {
        this.dealresult = dealresult;
    }

    public String getExceptiondesc() {
        return exceptiondesc;
    }

    public void setExceptiondesc(String exceptiondesc) {
        this.exceptiondesc = exceptiondesc;
    }

    public String getHighwarncode() {
        return highwarncode;
    }

    public void setHighwarncode(String highwarncode) {
        this.highwarncode = highwarncode;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLocate() {
        return locate;
    }

    public void setLocate(String locate) {
        this.locate = locate;
    }

    public int getPipeid() {
        return pipeid;
    }

    public void setPipeid(int pipeid) {
        this.pipeid = pipeid;
    }
}
